<script>
    Modernizr.load({
        test: Modernizr.csstransforms3d && Modernizr.csstransitions,
        yep : ['<?php echo base_url('assets/js/hoverfold.js'); ?>'],
        nope: '<?php echo base_url('assets/css/fallback.css'); ?>',
        callback : function( url, result, key ) {
            if( url === '<?php echo base_url('assets/js/hoverfold.js'); ?>' ) {
                $( '.content' ).hoverfold();
            }
        }
    });
    
    $(document).ready(function () {
        $("#isotope").isotope({
            itemSelector: '.content',
            layoutMode: 'masonry'
        });
        
        /* Styling front */
        $(".content").each(function(){
            var leftP = $(this).css("left");
            var leftPInt = leftP.replace("px","");
            if (leftPInt == 0 || leftPInt >= 500) {
                $(this).addClass("no-lr-border");
            }            
        });
        $(".content-img-icon").each(function(){
            var parentHeight = $(this).parent(".content-img").css("height");
            var parentHeightInt = parentHeight.replace("px","");
            $(this).css("height",parentHeightInt+"px");
        });
        /* End styling front */

        $("#search-ticket").keyup(function () {
            $(".dataTables_filter input[type='search']").val($(this).val());
            $(".dataTables_filter input[type='search']").keyup();
        });

        $("#ticketFormBtn").click(function () {
            $("#ticketForm").modal({
                backdrop: "static"
            });
        });


        $("#ticketAttachment").click(function () {
            $("#fileAttachment").click();
        });

        $("#fileAttachment").change(function () {
            $("#fileValue").html($(this).val());
        });

        $(".assign").click(function () {
            var ticketId = this.id;
            $.ajax({
                type: 'POST',
                url: '<?php echo base_url('ticket/ticket_assign'); ?>/' + ticketId,
                success: function () {
                    $(".ticket-status."+ticketId).html('Assigned');
                    $("a#"+ticketId).replaceWith('<?php echo $this->session->userdata('username'); ?>');
                    $("#ticket-detail-container").load('<?php echo base_url('ticket/ticket_detail'); ?>/' + ticketId);
                }
            });
        });
    });
</script>